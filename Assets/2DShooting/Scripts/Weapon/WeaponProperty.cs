using UnityEngine;
using System.Collections;

public class WeaponProperty  {
    /// <summary>
    /// 攻击力
    /// </summary>
    public float Power;
    /// <summary>
    /// 攻击速度 攻击次数/秒
    /// </summary>
    public float FireRate;
    /// <summary>
    /// 弹夹
    /// </summary>
    public int ClipSize;
    /// <summary>
    /// 子弹数量
    /// </summary>
    public int BulletCount;
    /// <summary>
    /// 得分加成
    /// </summary>
    public float ScoreBonus;
    /// <summary>
    /// 升级价格
    /// </summary>
    public int UpgPrice;
}
